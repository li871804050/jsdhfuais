package algorithm;

public class Degree {
	private Object key;
	
	private Object inDegree;
	
	private Object outDegree;

	public Object getKey() {
		return key;
	}

	public Object getInDegree() {
		return inDegree;
	}

	public Object getOutDegree() {
		return outDegree;
	}

	public void setKey(Object key) {
		this.key = key;
	}

	public void setInDegree(Object inDegree) {
		this.inDegree = inDegree;
	}

	public void setOutDegree(Object outDegree) {
		this.outDegree = outDegree;
	}
	

}
